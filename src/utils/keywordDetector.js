const normalizedKeywords = keywords.map((keyword) => ({
  original: keyword,
  normalized: keyword.toLocaleLowerCase('ja-JP'),
}));

function detectKeyword(content) {
  const normalizedContent = content.toLocaleLowerCase('ja-JP');

  // Array order determines the first match when one message contains several words.
  return normalizedKeywords.find(({ normalized }) =>
    normalizedContent.includes(normalized),
  )?.original ?? null;
}

